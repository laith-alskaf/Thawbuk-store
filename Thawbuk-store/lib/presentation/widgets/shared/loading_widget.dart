class ListLoadingWidget extends StatelessWidget {
  final int itemCount;

  const ListLoadingWidget({
    super.key,
    this.itemCount = 5,
  });

  @override
  Widget build(BuildContext context) {
    return ShimmerLoading(
      isLoading: true,
      child: ListView.builder(
        itemCount: itemCount,
        itemBuilder: (context, index) => const ListItemShimmer(),
      ),
    );
  }
}